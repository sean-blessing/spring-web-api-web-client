package com.example.springwebapp.model;

public class Joke {

    private int id;
    private String type;
    private String setup;
    private String punchline;

    public int getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getSetup() {
        return setup;
    }

    public String getPunchline() {
        return punchline;
    }
}
